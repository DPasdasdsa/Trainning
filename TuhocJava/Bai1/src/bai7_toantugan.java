public class bai7_toantugan {
    public static void main(String[] args) {
        // gán cộng
        int x=8;
        x+=5;
        System.out.println("x= "+x);
        // gán trừ
        int y=2;
        y-=2;
        System.out.println("y= "+y);
        // gán nhân
        int z=3;
        z*=3;
        System.out.println("z= "+z);
        // gán chia x/=3
        // gán chia phần dư x%=3;
        int k=8;
        double m = (double) k / 3; // Ép k sang double trước khi chia
        System.out.println("k= "+m);
        //>= lớn hơn hoặc bằng, <= nhỏ hơn hoặc bằng
    }
}
